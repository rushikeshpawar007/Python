# Comparisons:
# Equal:            3 == 2
# Not Equal:        3 != 2
# Greater Than:     3 > 2
# Less Than:        3 < 2
# Greater or Equal: 3 >= 2
# Less or Equal:    3 <= 2
# boolean values will be obtained


num_1 = 2
num_2 = 3

print(num_1 == num_2)
print(num_1 != num_2)
print(num_1 < num_2)
print(num_1 > num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)


